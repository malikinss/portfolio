num_1 = int(input())
num_2 = int(input())

sum = num_1 + num_2
diff = num_1 - num_2
prod = num_1 * num_2

print(num_1, '+', num_2, '=', sum)
print(num_1, '-', num_2, '=', diff)
print(num_1, '*', num_2, '=', prod)